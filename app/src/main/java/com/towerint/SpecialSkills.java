public class SpecialSkills {
    private double x;
    private double y;
    private double radius;
    private double popularityCost;

    public double getX(){
        return x;
    }
    public double getY(){
        return y;
    }
    public double getRadius(){
        return radius;
    }
    public double getPopularityCost(){
        return popularityCost;
    }
}
